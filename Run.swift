import Foundation

class Run {
    var distance: Double!
    var hours: Int!
    var minutes: Int!
    var year: Int!
    var month: Int!
    var day: Int!
    
    init(distance: Double, hours: Int, minutes: Int, year:Int, month:Int, day:Int) {
        self.distance = distance
        self.hours = hours
        self.minutes = minutes
        self.year = year
        self.month = month
        self.day = day
    }
    
    
}
